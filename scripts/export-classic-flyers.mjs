#!/usr/bin/env node
/**
 * Export pre–Bold Navy flyer HTML from git into public/marketing/classic/.
 * Does not modify main flyer-*.html (Bold Navy). Re-run after updating SOURCE_COMMIT if needed.
 *
 * Usage: node scripts/export-classic-flyers.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';

/** Last commit before unified Bold Navy generator overwrote distinct layouts. */
const SOURCE_COMMIT = '4a8da8d1';

const OUT_DIR = path.join(process.cwd(), 'public', 'marketing', 'classic');
const MANIFEST_TS = path.join(process.cwd(), 'src', 'lib', 'classicFlyerManifest.ts');

function listFlyerFilesAt(commit) {
  const out = execSync(`git ls-tree --name-only ${commit} public/marketing/`, { encoding: 'utf8' });
  return out
    .split('\n')
    .map((line) => line.trim())
    .filter((line) => /^public\/marketing\/flyer-.+\.html$/.test(line))
    .map((line) => path.basename(line));
}

function exportFile(filename) {
  const content = execSync(`git show ${SOURCE_COMMIT}:public/marketing/${filename}`, {
    encoding: 'utf8',
    maxBuffer: 10 * 1024 * 1024,
  });
  fs.writeFileSync(path.join(OUT_DIR, filename), content, 'utf8');
}

fs.mkdirSync(OUT_DIR, { recursive: true });

const files = listFlyerFilesAt(SOURCE_COMMIT);
for (const file of files) {
  exportFile(file);
  console.log('exported', file);
}

const manifestBody = `/**
 * Flyer HTML files with original per-layout styles (exported from git).
 * Generated by \`node scripts/export-classic-flyers.mjs\` — do not edit by hand.
 */
export const CLASSIC_FLYER_FILES = ${JSON.stringify(files, null, 2)} as const;

export type ClassicFlyerFile = (typeof CLASSIC_FLYER_FILES)[number];

export function hasClassicFlyer(href: string): boolean {
  const name = href.replace(/^\\/marketing\\//, '').replace(/^\\/marketing\\/classic\\//, '');
  return (CLASSIC_FLYER_FILES as readonly string[]).includes(name);
}

export function classicFlyerHref(href: string): string {
  const name = href.replace(/^\\/marketing\\//, '').replace(/^\\/marketing\\/classic\\//, '');
  return \`/marketing/classic/\${name}\`;
}
`;

fs.writeFileSync(MANIFEST_TS, manifestBody, 'utf8');
console.log(`\nWrote ${files.length} files to ${OUT_DIR}`);
console.log(`Manifest: ${MANIFEST_TS}`);
